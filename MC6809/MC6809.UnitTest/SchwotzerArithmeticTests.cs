// Test data from W. Schwotzer's MC6809 CPU Emulation Validation suite,
// verified on an SGS Thomson EF6809P processor.

namespace MC6809.UnitTest
{
    [TestClass]
    public class SchwotzerArithmeticTests
    {
        private readonly Board board = new();
        private readonly MC6809 cpu;

        public SchwotzerArithmeticTests() => this.cpu = this.board.CPU;

        [TestInitialize]
        public void Initialise() { this.board.RaisePOWER(); this.cpu.Step(); }

        [TestCleanup]
        public void Cleanup() => this.board.LowerPOWER();

        // ADD (ADDB immediate): checks HNZVC ($2F)
        [DataTestMethod]
        [DataRow(0x00, 0x00, 0x00, 0x04)]
        [DataRow(0x00, 0x01, 0x01, 0x00)]
        [DataRow(0x40, 0x40, 0x80, 0x0A)]
        [DataRow(0x00, 0x80, 0x80, 0x08)]
        [DataRow(0x80, 0x80, 0x00, 0x07)]
        [DataRow(0x40, 0x80, 0xC0, 0x08)]
        [DataRow(0x7F, 0x7F, 0xFE, 0x2A)]
        [DataRow(0x7F, 0x80, 0xFF, 0x08)]
        [DataRow(0x80, 0x7F, 0xFF, 0x08)]
        [DataRow(0x7F, 0x01, 0x80, 0x2A)]
        [DataRow(0x80, 0x01, 0x81, 0x08)]
        [DataRow(0x40, 0xC0, 0x00, 0x05)]
        [DataRow(0x40, 0xC1, 0x01, 0x01)]
        [DataRow(0xFF, 0xFF, 0xFE, 0x29)]
        [DataRow(0x81, 0x80, 0x01, 0x03)]
        public void TestADDB(int operand, int data, int expected, int flags)
        {
            this.board.Poke(0, 0xCB);
            this.board.Poke(1, (byte)data);
            this.cpu.CC = 0;
            this.cpu.B = (byte)operand;
            this.cpu.Step();
            Assert.AreEqual((byte)expected, this.cpu.B);
            Assert.AreEqual((byte)flags, (byte)(this.cpu.CC & 0x2F));
        }

        // ADDD (immediate): checks NZVC ($0F)
        [DataTestMethod]
        [DataRow(0x0000, 0x0000, 0x0000, 0x04)]
        [DataRow(0x0000, 0x0001, 0x0001, 0x00)]
        [DataRow(0x4000, 0x4000, 0x8000, 0x0A)]
        [DataRow(0x0000, 0x8000, 0x8000, 0x08)]
        [DataRow(0x8000, 0x8000, 0x0000, 0x07)]
        [DataRow(0x4000, 0x8000, 0xC000, 0x08)]
        [DataRow(0x7FFF, 0x7FFF, 0xFFFE, 0x0A)]
        [DataRow(0x7FFF, 0x8000, 0xFFFF, 0x08)]
        [DataRow(0x7FFF, 0x0001, 0x8000, 0x0A)]
        [DataRow(0x8000, 0x0001, 0x8001, 0x08)]
        [DataRow(0x4000, 0xC000, 0x0000, 0x05)]
        [DataRow(0x4000, 0xC001, 0x0001, 0x01)]
        [DataRow(0xFFFF, 0xFFFF, 0xFFFE, 0x09)]
        [DataRow(0x8001, 0x8000, 0x0001, 0x03)]
        public void TestADDD(int operand, int data, int expected, int flags)
        {
            this.board.Poke(0, 0xC3);
            this.board.Poke(1, (byte)(data >> 8));
            this.board.Poke(2, (byte)(data & 0xFF));
            this.cpu.CC = 0;
            this.cpu.D.Word = (ushort)operand;
            this.cpu.Step();
            Assert.AreEqual((ushort)expected, this.cpu.D.Word);
            Assert.AreEqual((byte)flags, (byte)(this.cpu.CC & 0x0F));
        }

        // ADC (ADCA immediate): carry-in from carryIn; checks HNZVC ($2F)
        [DataTestMethod]
