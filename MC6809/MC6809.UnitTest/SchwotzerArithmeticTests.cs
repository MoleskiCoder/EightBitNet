        [DataRow(0xFF, 1, 0xFF, 0xFF, 0x29)]
        [DataRow(0x81, 1, 0x80, 0x02, 0x03)]
        public void TestADCA(int operand, int carryIn, int data, int expected, int flags)
        {
            this.board.Poke(0, 0x89);
